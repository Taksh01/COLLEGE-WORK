#include <iostream>
using namespace std;

int main()
{
    int a = 10;
    int b = 5;
    cout << " Add is: " << a + b;
    cout << "\n Sub is: " << a - b << endl;
    cout << " Div is: " << a / b;
    cout << " \n Modulo is: " << a % b;
    cout << " \n Multi is: " << a * b;
    cout << " \n Incriment is: " << ++a;
    cout << " \n Decriment is: " << --b;
}
